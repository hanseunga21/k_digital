package a0102;

public class Escape {
    public static void main(String[] args) {
        //탭(\t)과 줄바꿈(\n)
        System.out.println("이름\t나이\t주소");
        System.out.println("홍길동\t20\t서울");
        // 이름    나이    주소
        // 홍길동  20      서울
        
        //줄바꿈
        System.out.println("안녕하세요\n반갑습니다.");

        
    }
}
